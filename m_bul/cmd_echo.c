#include "bul_cmd.h"

int	cmd_echo(t_env *env, int nl, char **arg)
{
	int i;
	int s;

	i = 0;
	s = 0;
	while (arg[i])
	{
		if (s == 1)
			printf(" ");
		printf("%s", arg[i]);
		s = 1;
		i++;
	}
	if (nl)
		printf("\n");
}
