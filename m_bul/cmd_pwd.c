#include "../ex.h"

int	cmd_pwd()
{
	char	*pwd;

	pwd = getcwd(NULL, 0);
	if (!pwd)
	{
		write(2, "Error: getcwd()", 15);
		return (1);
	}
	printf("%s\n", pwd);
	free(pwd);
	return (0);
}
