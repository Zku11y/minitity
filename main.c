int main(int ac, char **av, char **env)
{
	t_data *data;

	signal(SIGINT, hnd_sig);
	signal(SIGQUIT, SIG_IGN);
	data = ft_calloc(sizeof(data), 1);
	data->fd = 1;
	data->env = int_env(env);
	while(1)
	{
		if(prompt_msg() == 0)
			return 0;
		printf("\n");
	}
	fre_env(data->env);
	free(data);
	return 0;
}











