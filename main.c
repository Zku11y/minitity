static void hnd_sig(int sig)
{
	(void)sig;
	write(1, "\n", 1);
	rl_replace_line("", 0);
	rl_on_new_line();
	rl_redisplay();
}

int prompt_msg()
{
	char *line;

	line = readline("\e[1;32m❯ \e[0m");
	if (!line)
	{
		write(1, "exit\n", 5);
		free(line);
		exit(69);
	}
	if(ft_strncmp(line, "exit", 5) == 0 || ft_strncmp(line, "exit ", 5) == 0)
		return (free(line), 0);
	free(line);
	return 1;
}

